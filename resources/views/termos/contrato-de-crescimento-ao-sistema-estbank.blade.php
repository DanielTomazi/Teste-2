<p><u><strong>5. TRANSAÇÃO</strong></u></p>
<p>5.1. A TRANSAÇÃO deverá observar todas as condições do contrato e seus anexos, bem como, as condições e regras operacionais e de segurança que venham a ser instituídas a qualquer tempo pelo ESTBANK, pelas BANDEIRAS, por lei ou por regulamentação.</p>
<p>5.2. O ESTABELECIMENTO não poderá efetuar TRANSAÇÕES em segmentos ou ramos de atividade diferentes daqueles cadastrados no SISTEMA ESTBANK e/ou em desacordo com seu objeto social, sem prévia comunicação e autorização do ESTBANK e tampouco a realizar atividades que representem infração a leis ou regulamentos vigentes no país ou que sejam vedados pelas BANDEIRAS.</p>
<p>5.3. As TRANSAÇÕES com CARTÕES realizadas pelo ESTABELECIMENTO poderão ser processadas por quaisquer CREDENCIADORAS integrantes do SISTEMA ESTBANK.</p>
<p>5.4. As TRANSAÇÕES com indícios ou suspeitas de fraude estarão sujeitas ao não processamento
ou ao cancelamento, ainda que realizada de forma conivente ou não pelo ESTABELECIMENTO. </p>
<p>5.5. O ESTABELECIMENTO deverá utilizar o SISTEMA ESTBANK somente para realizar TRANSAÇÕES
regulares, sendo vedada a realização de TRANSAÇÕES fictícias ou simuladas.</p>
<p>5.6. No momento da realização da TRANSAÇÃO, o ESTABELECIMENTO deve, obrigatoriamente: (i)
verificar se o prazo de validade do CARTÃO não está vencido e se o CARTÃO não está adulterado ou
rasurado; (ii) conferir se o nome do PORTADOR confere com os documentos oficiais de identificação
do PORTADOR; (iii) comparar os últimos 4 (quatro) dígitos do número do CARTÃO com aqueles
impressos no comprovante de venda; (iv) conferir a existência do código de segurança, formado por
três dígitos, no verso do CARTÃO; e (v) observar as características de segurança utilizadas pelas
BANDEIRAS, em especial a existência no CARTÃO, de chip, holograma tridimensional, tarja
magnética, dentre outras.</p>
<p>5.7. Caso a TRANSAÇÃO seja realizada na modalidade CRÉDITO, o ESTABELECIMENTO deverá
indicar, no campo próprio do POS, a opção de pagamento escolhida pelo PORTADOR, dentre as
seguintes modalidades:</p>
<p>a) Transação à Vista: TRANSAÇÃO na qual o VALOR LÍQUIDO será creditado no DOMICÍLIO
BANCÁRIO do ESTABELECIMENTO em uma única parcela, no prazo estabelecido no TERMO DE
CREDENCIAMENTO;</p>
<p>b) Parcelado EMISSOR: TRANSAÇÃO na qual o VALOR LÍQUIDO DA TRANSAÇÃO será creditado no
DOMICÍLIO BANCÁRIO do ESTABELECIMENTO em uma única parcela, no prazo estabelecido no
TERMO DE CREDENCIAMENTO; e</p>
<p>c) Parcelado Estabelecimento: TRANSAÇÃO na qual o VALOR LÍQUIDO DA TRANSAÇÃO será
creditado no DOMICÍLIO BANCÁRIO do ESTABELECIMENTO em parcelas mensais, iguais e sucessivas,
sendo a primeira delas creditada no prazo estabelecido no TERMO DE CREDENCIAMENTO, e as
demais, nas mesmas datas dos meses subsequentes.</p>
<p>5.8. Caso a TRANSAÇÃO seja realizada na modalidade débito, o ESTABELECIMENTO, deverá
indicar, no campo próprio do POS; sendo que, neste caso, a TRANSAÇÃO deverá ser obrigatoriamente
realizadas mediante e digitação da senha pessoal pelo PORTADOR.</p>
<p>5.9. O ESTABELECIMENTO é o único e exclusivo responsável por eventuais erros na indicação: (i)
da opção de pagamento escolhida pelo PORTADOR; (ii) do valor da TRANSAÇÃO; (iii) da quantidade
de parcelas; (iv) do cancelamento indevido de TRANSAÇÃO; e/ou (v) da opção de estorno quando da
realização da TRANSAÇÃO.</p>
<p>5.10. O ESTABELECIMENTO reconhece e aceita que o ESTBANK poderá, a seu exclusivo critério,
solicitar alterações nos procedimentos de realização das TRANSAÇÕES, de forma a obter maior
segurança.</p>
<p>5.11. O ESTABELECIMENTO se responsabiliza integralmente pela TRANSAÇÃO, devendo solucionar
diretamente com o PORTADOR toda e qualquer controvérsia sobre os bens e serviços fornecidos,
incluindo casos de defeito, vícios ou devolução, fraude, problemas na entrega, etc., e se
responsabiliza integralmente pela TRANSAÇÃO, e, ainda, isenta o ESTBANK de qualquer
responsabilidade relativa a esses bens e serviços, inclusive com relação ao Código de Proteção e
Defesa do Consumidor e demais legislações aplicáveis, bem como indenizando o ESTBANK em caso
de imputação de responsabilidade pelas situações aqui previstas.</p>
<p>5.12. O ESTABELECIMENTO está impedido de impor condições e/ou restrições ao pleno uso e a
aceitação de cartões e meios de pagamento, sendo-lhe expressamente vedado efetuar qualquer
discriminação relativamente a quaisquer emissores ou bandeiras.</p>
<p><u><strong>6. CHARGEBACK E CANCELAMENTO DAS TRANSAÇÕES</strong></u></p>
<p>6.1. Fica estabelecido que são aplicáveis ao ESTABELECIMENTO as regras de CHARGEBACK
estipuladas pelas CREDENCIADORAS, conforme contratos de credenciamento (que o
ESTABELECIMENTO declara conhecer), os quais podem ser verificados nos sites das
CREDENCIADORAS. Também se aplicam ao ESTABELECIMENTO as regras estipuladas pelas
instituições financeiras vinculadas às transações realizadas por meio de boleto bancário.</p>
<p>6.2. A AUTORIZAÇÃO do EMISSOR, ainda que enviado código de autorização, não caracteriza a
regularidade da TRANSAÇÃO, apenas a validade do CARTÃO e a existência de limite de crédito do
PORTADOR para a realização da TRANSAÇÃO, sendo possível, posteriormente, a aplicação de
CHARGEBACK e o estorno ou não do pagamento da TRANSAÇÃO ao ESTABELECIMENTO.</p>
<p>6.3. O ESTBANK irá aplicar o CHARGEBACK e debitar o valor da TRANSAÇÃO respectiva dos créditos
futuros, sempre que: (i) o EMISSOR OU PORTADOR DO CARTÃO apresentar contestação da
TRANSAÇÃO em razão de suspeita de fraude ou qualquer irregularidade; ou (ii) não cumprimento,
pelo ESTABELECIMENTO, dos termos deste Contrato, dos Anexos e/ou das orientações do ESTBANK.</p>
<p>6.4. O ESTABELECIMENTO deverá, em até 02 (dois) dias úteis contados da solicitação pelo
ESTBANK, fornecer a documentação referente à comprovação da entrega dos produtos ou serviços,
para afastar a contestação por CHARGEBACK, incluindo mas não limitando-se às cópias legíveis e sem
rasuras do COMPROVANTE DE TRANSAÇÃO ou da NOTA FISCAL DE VENDA ou PRESTAÇÃO DE
SERVIÇO correspondente à TRANSAÇÃO efetuada pelo PORTADOR DO CARTÃO e comprovação da
entrega dos bens adquiridos ou da prestação de serviços realizada.</p>
<p>6.5. O ESTBANK poderá deixar de pagar a TRANSAÇÃO ou descontar de seus créditos futuros, a
quantia equivalente às contestações por CHARGEBACK, caso não sejam apresentados os
comprovantes de entrega do produto ou da prestação do serviço.</p>
<p>6.6. O CHARGEBACK poderá ser aplicado em até 21 (vinte um) meses contados da realização da
TRANSAÇÃO, e mesmo que haja a AUTORIZAÇÃO DO EMISSOR e o pagamento da TRANSAÇÃO pelo
ESTBANK, de acordo com as regras estipuladas pelas BANDEIRAS e CREDENCIADORAS</p>
<p>6.7. Nas hipóteses de CHARGEBACK, o ESTBANK poderá: (i) compensar o valor com quaisquer
outros créditos, existentes ou futuros, decorrentes das TRANSAÇÕES realizadas pelo
ESTABELECIMENTO; ou (ii) na inexistência de créditos, realizar a cobrança por quaisquer meios
cabíveis e por parceiros autorizados, inclusive mediante o envio de boletos ou cobranças; estando
autorizada a incluir o débito nos serviços de proteção ao crédito em caso de inadimplemento, bem
como a que o ESTBANK apresente à protesto o contrato e/ou os boletos em aberto ao cartório
competente.</p>
<p>6.8. Em caso de atraso no pagamento de valores devidos ao ESTBANK, o referido montante deverá
ser restituído pelo ESTABELECIMENTO com correção monetária, sendo utilizada no mínimo a
variação do IGP-M/FGV, mais juros de 1% (um por cento) ao mês ou fração pro-rata, desde a data do
débito até o efetivo pagamento, acrescido de multa de 10% (dez por cento), dos encargos
operacionais e perdas e danos incorridos.</p>
<p>6.9. O ESTABELECIMENTO poderá solicitar o cancelamento das TRANSAÇÕES realizadas no
SISTEMA ESTBANK no prazo máximo de 05 (cinco) dias úteis contados da realização da respectiva
TRANSAÇÃO, mediante solicitação formal ao ESTBANK.</p>
<p>6.9.1. Se o pagamento da TRANSAÇÃO já tiver sido realizado ao ESTABELECIMENTO, mesmo por
antecipação, o ESTBANK irá compensar o respectivo valor com os créditos futuros do
ESTABELECIMENTO.</p>
<p>6.10. Em caso de inexistência ou insuficiência de créditos a compensar, o cancelamento será
negado, e o ESTABELECIMENTO deverá resolver diretamente com os PORTADORES a devolução dos
valores respectivos.</p>
<p>6.11. Aplicam-se ao ESTABELECIMENTO as respectivas multas e penalidades originalmente
aplicáveis ao ESTBANK pelas BANDEIRAS e CREDENCIADORAS, as quais o ESTABELECIMENTO
compromete-se a pagar imediatamente ao ESTBANK, sob pena de serem cobradas por quaisquer das
formas estipuladas neste contrato.</p>
<p>6.12. Os prazos e procedimentos aplicáveis ao CHARGEBACK serão aqueles definidos pelas
BANDEIRAS e CREDENCIADORAS; cujos instrumentos respectivos se encontram à disposição do
ESTABELECIMENTO, mediante solicitação prévia ao ESTBANK.</p>
<p><u><strong>7. PAGAMENTO</strong></u></p>
<p>7.1. O ESTBANK efetuará o pagamento das TRANSAÇÕES realizadas no SISTEMA ESTBANK,
mediante repasse do respectivo VALOR LÍQUIDO para o DOMICÍLIO BANCÁRIO do
ESTABELECIMENTO, no prazo acordado com o ESTBANK, observadas as condições aqui estabelecidas
e desde que a TRANSAÇÃO tenha sido realizada de acordo com este contrato.</p>
<p>7.2. O pagamento será realizado pelo Valor Líquido, após descontada: i) taxas e tarifas devidas ao
ESTBANK, conforme prevista neste Contrato ou Anexos; ii) o valor relativo ao aluguel pela locação do
POS, quando cabível; iii) outros valores devidos</p>
<p>7.3. Efetuado o crédito no DOMICÍLIO BANCÁRIO do ESTABELECIMENTO, estará comprovada, para
todos os efeitos, a quitação das obrigações pecuniárias decorrentes da TRANSAÇÃO, ficando apenas,
sujeito ao cancelamento, débito e/ou estorno nas hipóteses previstas neste instrumento.</p>
<p>7.4. Caberá ao ESTBANK emitir a nota fiscal sobre os serviços que forem prestados ao
ESTABELECIMENTO, se aplicável, bem como proceder a retenção de todos os impostos que incidam
sobre Tarifa por TRANSAÇÃO, nos termos da legislação vigente.</p>
<p>7.5. VALOR LÍQUIDO DA TRANSAÇÃO será pago pelo ESTBANK de acordo com os prazos definidos
no TERMO DE CREDENCIAMENTO, que poderão ser distintos, a depender da CREDENCIADORA,
BANDEIRA do CARTÃO ou para cartões de benefícios. </p>
<p>7.5.1. Quando decorrente de falha técnica e/ou operacional no SISTEMA ESTBANK e/ou de defeitos
nos POS, o ESTBANK poderá, sem incorrer em qualquer ônus ou penalidade, efetuar o pagamento do
VALOR LÍQUIDO na data subsequente, de acordo com o fluxo de pagamento contratado.</p>
<p>7.6. O ESTABELECIMENTO terá acesso aos VALORES LÍQUIDOS DAS TRANSAÇÕES pendentes de
pagamento mediante acesso às FUNCIONALIDADES, podendo visualizar o saldo e o extrato das
movimentações. A disponibilização do saldo e do extrato caracteriza-se como prestação de contas,
para todos os fins legais.</p>
<p>7.7. O ESTABELECIMENTO terá o prazo de 30 (trinta) dias para apontar eventual divergência ou
incorreção em relação a qualquer um dos valores pagos em seu DOMICÍLIO BANCÁRIO, inclusive das
retenções e compensações realizadas pela aplicação do CHARGEBACK. Após esse prazo, o
ESTABELECIMENTO dará a plena e definitiva quitação ao ESTBANK, não restando direito de
reclamação pelo ESTABELECIMENTO.</p>
<p><u><strong>8. ANTECIPAÇÃO DO PAGAMENTO DAS TRANSAÇÕES</strong></u></p>
<p>8.1. O ESTABELECIMENTO poderá solicitar ao ESTBANK o recebimento antecipado do VALOR
LÍQUIDO DAS TRANSAÇÕES, por meio dos canais de atendimento, ficando ao exclusivo critério do
ESTBANK antecipar ou não o pagamento das TRANSAÇÕES.</p>
<p>8.2. A solicitação de antecipação do pagamento está sujeita à análise, por critérios próprios do
ESTBANK, das TRANSAÇÕES realizadas e da situação financeira do ESTABELECIMENTO.</p>
<p>8.3. Ainda que previsto contratualmente, que o ESTABELECIMENTO possua TRANSAÇÕES a serem
liquidadas pelo SISTEMA ESTBANK e/ou tenha havido antecipações anteriores, o ESTBANK não é
obrigado a antecipar o pagamento dos recebíveis de novas TRANSAÇÕES.</p>
<p>8.4. O valor da taxa de antecipação e as demais condições comerciais poderão ser pactuadas no
TERMO DE CREDENCIAMENTO ou em cada solicitação de antecipação (podendo, nesse caso, sofrer
alterações de acordo com o horário e dia).</p>
<p>8.5. O ESTABELECIMENTO poderá, mediante a disponibilidade e aprovação prévia pela ESTBANK,
optar por realizar a antecipação automática do VALOR LÍQUIDO DAS TRANSAÇÕES. Neste caso, as
partes deverão definir previamente, no TERMO DE CREDENCIAMENTO ou em outro documento
específico, o valor da taxa de antecipação que será aplicada e as condições de pagamento.</p>
<p>8.5.1. O ESTBANK poderá alterar, a qualquer tempo e de maneira unilateral, as condições
previamente definidas para a antecipação automática, mediante comunicação prévia por escrito, via
e-mail, ao ESTABELECIMENTO.</p>
<p>8.6. Nas operações de recebimento antecipado, o ESTABELECIMENTO desde já reconhece e aceita
que é responsável pela legitimidade das vendas, sendo responsável ainda pelos débitos, estornos,
cancelamentos ocorridos mesmo após a formalização da antecipação, devendo, nesta hipótese,
ressarcir o ESTBANK de tais valores, acrescidos de juros de 1% ao mês e correção monetária pela
variação positiva do IGPM/FGV ou outro índice que vier a substituí-lo, os quais poderão ser deduzidos
pelo SISTEMA ESTBANK ou debitados de seu DOMICÍLIO BANCÁRIO ou cobrados por meio de boleto
bancário, a critério do ESTBANK.</p>
<p><u><strong>9. DOMICÍLIO BANCÁRIO</strong></u></p>
<p>9.1. Quando do credenciamento, o ESTABELECIMENTO deverá cadastrar DOMICÍLIO BANCÁRIO,
perante uma das instituições financeiras integrantes do SISTEMA ESTBANK para recebimento do
VALOR LÍQUIDO decorrente das TRANSAÇÕES.</p>
<p>9.2. O ESTABELECIMENTO é responsável por manter a regularidade do DOMICÍLIO BANCÁRIO.
Caso a instituição financeira do DOMICÍLIO BANCÁRIO declare-se impedida, por qualquer motivo, de
dar cumprimento às ordens de crédito emitidas pelo ESTBANK, deverá o ESTABELECIMENTO
providenciar sua regularização ou cadastrar novo DOMICÍLIO BANCÁRIO.</p>
<p>9.3. O ESTBANK está autorizado a reter o pagamento das TRANSAÇÕES até que haja a
regularização do DOMICÍLIO BANCÁRIO cadastrado no SISTEMA ESTBANK, sem que incorra em
quaisquer ônus, penalidades ou encargos.</p>
<p>9.4. Na hipótese de a data prevista para o crédito do VALOR LÍQUIDO DAS TRANSAÇÕES ser
considerada feriado ou em dia de não funcionamento bancário na praça de compensação da conta
do DOMICÍLIO BANCÁRIO do ESTABELECIMENTO, o pagamento será realizado no primeiro dia útil
subsequente.</p>
<p>9.5. O ESTABELECIMENTO poderá solicitar a alteração de seu DOMICÍLIO BANCÁRIO, com 30
(trinta) dias de antecedência, mediante solicitação por meio das FUNCIONALIDADES.</p>
<p>9.6. O pagamento do VALOR LÍQUIDO DAS TRANSAÇÕES capturadas anteriormente à alteração
poderá ser realizado no DOMICÍLIO BANCÁRIO então vigente.</p>
<p>9.7. O ESTABELECIMENTO declara-se ciente de que não poderá ser realizada a alteração do
DOMICÍLIO BANCÁRIO, caso haja TRAVA DE DOMICÍLIO BANCÁRIO, definida na cláusula a seguir.</p>
<p>9.8. O ESTABELECIMENTO autoriza o ESTBANK a enviar e manter atualizadas junto a Câmara
Interbancária de Pagamentos - CIP as informações relativas (i) ao credenciamento e à manutenção
de DOMICÍLIO BANCÁRIO e (ii) à existência ou não de antecipação do pagamento das TRANSAÇÕES.</p>
<p>9.9. Em caso de término do CONTRATO por qualquer motivo, o ESTABELECIMENTO se
compromete a manter ativo seu DOMICÍLIO BANCÁRIO até que todas as TRANSAÇÕES sejam
liquidadas.</p>
<p><u><strong>10. REMUNERAÇÃO DO ESTBANK</strong></u></p>
<p>10.1. Em contrapartida à prestação dos serviços de captura, processamento, roteamento e
liquidação das TRANSAÇÕES, o ESTABELECIMENTO pagará ao ESTBANK a TARIFA por TRANSAÇÃO,
incidente sobre o VALOR BRUTO de cada TRANSAÇÃO realizada no SISTEMA ESTBANK, nos termos
do ANEXO I - TERMO DE ADESÃO AO SISTEMA ESTBANK</p>
<p>10.2. A TARIFA por TRANSAÇÃO poderá variar conforme o segmento ou ramo de atuação do
ESTABELECIMENTO, sua localização, forma de captura da TRANSAÇÃO, volume de captura, entre
outros critérios adotados pelo ESTBANK, razão pela qual é proibido o ESTABELECIMENTO utilizar os
serviços do ESTBANK e o POS em outros ramos de atuação e em diferente localização da cadastrada
no ANEXO I - TERMO DE ADESÃO AO SISTEMA ESTBANK.</p>
<p>10.3. Ainda, o ESTBANK poderá cobrar taxas e tarifas pelos SERVIÇOS adicionais prestados ao
ESTABELECIMENTO:</p>
<p>a. Taxa de Antecipação: devida caso haja a antecipação do pagamento do VALOR LÍQUIDO DAS
TRANSAÇÕES;</p>
<p>b. Tarifa de adesão pelo credenciamento do ESTABELECIMENTO ao SISTEMA ESTBANK;</p>
<p>c. Tarifa de Extrato: devida pela disponibilização de extratos impressos, relatórios de conciliação ou
outros documentos solicitados pelo ESTABELECIMENTO;</p>
<p>d. Tarifa de Instalação de POS: devida para a instalação do equipamento contratado pelo
ESTABELECIMENTO no local em que será utilizado;</p>
<p>e. Tarifa pela retirada e desinstalação do POS no ESTABELECIMENTO;</p>
<p>f. Tarifa Cancelamento de TRANSAÇÃO ou CHARGEBACK: devida por consequência do cancelamento
da TRANSAÇÃO ou aplicação do CHARGEBACK;</p>
<p>g. Tarifa de Manutenção: remuneração mensal que será devida pelo ESTABELECIMENTO pela
utilização do SISTEMA ESTBANK;</p>
<p>h. Taxas Operacionais: devida em decorrência de procedimentos administrativos e/ou judiciais, tais
como, cumprimento de ofícios, bloqueios, penhoras e arrestos, a ser cobrada por cada evento;</p>
<p>i. Tarifa de Conciliação: devida pela prestação de serviço de conciliação de TRANSAÇÕES do
ESTABELECIMENTO junto às CREDENCIADORAS.</p>
<p>10.4. Os valores cobrados pelo ESTBANK são variáveis de acordo com a natureza de cada operação
realizada e poderão ser reajustados ou alterados unilateralmente e sem prévio aviso, encontrandose sempre disponíveis para consulta pelo ESTABELECIMENTO mediante acesso ao site do ESTBANK
ou mediante solicitação pelos canais de atendimento</p>
<p>10.5. O ESTABELECIMENTO, ao se credenciar ao SISTEMA ESTBANK através deste instrumento,
declara ciência e aceita que também adere às taxas e tarifas descritas no site do ESTBANK, e, ainda,
confirma o seu conhecimento de que as taxas e tarifas ali descritas são variáveis e representam a
remuneração devida ao ESTBANK e de terceiros.</p>
<p>10.6. Os pagamentos ao ESTBANK serão efetuados à vista, mediante débito do valor
correspondente antes do repasse para o DOMICÍLIO BANCÁRIO, mediante compensação com os
créditos devidos ao ESTABELECIMENTO em razão das TRANSAÇÕES realizadas.</p>
<p>10.7. Caso não haja recursos suficientes, o ESTBANK irá, automaticamente e sem aviso prévio,
realizar a compensação com eventuais créditos futuros do ESTABELECIMENTO.</p>
<p>10.8. Sem prejuízo da suspensão dos SERVIÇOS, caso o ESTABELECIMENTO não possua créditos a
serem compensados, o ESTBANK realizará a cobrança dos valores devidos, acrescidos de juros de 1%
ao mês e correção monetária pela variação positiva do IGPM/FGV ou outro índice que vier a substituílo mais multa moratória de 10% (dez por cento).</p>
<p>10.9. O ESTBANK poderá efetuar reajuste dos valores de quaisquer taxas, tarifas ou outras formas
de remuneração, bem como criar novas taxas, tarifas e outras formas de remuneração, devendo
informar previamente o ESTABELECIMENTO por e-mail ou WhatsApp</p>
<p>10.10. Caso o ESTABELECIMENTO não concorde com as novas condições de remuneração, poderá
solicitar esclarecimentos e, se, ainda assim, não concordar, poderá encerrar o contrato. O não
encerramento do contrato e utilização dos SERVIÇOS pelo ESTABELECIMENTO será interpretado
como plena anuência aos novos valores.</p>
<p>10.11. Caso sejam alteradas as condições comerciais do ESTBANK com as CREDENCIADORAS,
EMISSORES ou BANDEIRAS, ou sejam criados tributos ou alteradas as condições de cálculo e/ou
cobrança de tributos já existentes, os custos resultantes poderão ser repassados ao
ESTABELECIMENTO e somados à remuneração vigente, de forma a restabelecer o equilíbrio
econômico-financeiro da prestação dos SERVIÇOS.</p>
