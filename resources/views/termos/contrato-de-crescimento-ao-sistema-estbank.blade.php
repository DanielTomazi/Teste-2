TRANSAÇÕES pelo SISTEMA ESTBANK, independentemente de realizá-las e assim permanecerá por
prazo indeterminado</p>
<p>11.2. O ESTABELECIMENTO será considerado apto e habilitado na ocorrência de um dos seguintes
eventos: (i) envio de comunicação, pelo ESTBANK ao ESTABELECIMENTO, informando o
credenciamento do ESTABELECIMENTO ao SISTEMA ESTBANK; e/ou (ii) Emissão de ordem de
instalação, homologação e/ou indicação do número lógico do POS.</p>
<p>11.3. Este contrato poderá ser resilido, integral ou parcialmente, sem ônus ou multa, por qualquer
parte, a qualquer tempo, mediante envio de aviso prévio por escrito à outra parte, com 30 (trinta)
dias de antecedência responsabilizando-se as partes, nos termos e condições do presente, pelas
